package Refactor;

/**
 * Ejemplo de refactorizar un constructor
 */
public class ConstructorLargo {
    String marca;
    String motor;
    String modelo;
    int puertas;

    public ConstructorLargo(String marca, String motor, String modelo, int puertas) {
        this.marca = marca;
        this.motor = motor;
        this.modelo = modelo;
        this.puertas = puertas;
    }
}
