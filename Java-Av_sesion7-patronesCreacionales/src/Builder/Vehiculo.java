package Builder;

public class Vehiculo {
//Atributos
    public String marca;
    public String tipo;
    public String motor;
    public int puertas;

//Getters TRADICIONALES
    public String getMarca() {
        return marca;
    }


    public String getTipo() {
        return tipo;
    }


    public String getMotor() {
        return motor;
    }


    public int getPuertas() {
        return puertas;
    }


}
